class PegawaiTetap extends Pegawai {
    // Atribut
    private double upah;

    // Konstruktor
    public PegawaiTetap(String nama, String noKTP, double upah) {
        super(nama, noKTP);
        this.upah = upah;
    }

    // Implementasi method hitungGaji() untuk Pegawai Tetap
    @Override
    public double hitungGaji() {
        return upah;
    }
}
